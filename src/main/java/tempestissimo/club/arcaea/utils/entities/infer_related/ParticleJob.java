package tempestissimo.club.arcaea.utils.entities.infer_related;

public class ParticleJob {
    public double x;
    public double y;
    public double z;
    public int frame;
    public String type;

    public ParticleJob(double x, double y, double z, int frame, String type) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.frame = frame;
        this.type = type;
    }
}
